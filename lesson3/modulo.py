# The Modulo operator enables you to find the remainder

int0 = 5
int1 = 3

# 4/3 = 1R1 -> 3 goes into 4 one time, 4-3 = 1

# The quotient of int0 and int1
quotient = int(int0 / int1)
print(f"The quotient is: {quotient}")           # 1.333333->

# The remainder of int0 divided by int1
remainder = int0 % int1
print(f"The remainder is: {remainder}")         # 1
